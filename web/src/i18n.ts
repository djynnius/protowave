export const LOCALES: [string, string][] = [
  ['en', 'English'],
  ['de', 'Deutsch'],
  ['es', 'Español'],
  ['fr', 'Français'],
  ['ru', 'Русский'],
  ['zh-TW', '繁體中文'],
  ['sl', 'Slovenščina'],
]

function initialLocale(): string {
  const saved = localStorage.getItem('pw-locale')
  if (saved) return saved
  const nav = navigator.language
  if (nav.startsWith('zh')) return 'zh-TW'
  const short = nav.slice(0, 2)
  return LOCALES.some(([code]) => code === short) ? short : 'en'
}

export const i18n = createI18n({
  legacy: false,
  locale: initialLocale(),
  fallbackLocale: 'en',
  messages: { en, de, es, fr, ru, 'zh-TW': zhTW, sl },
})

export function setLocale(code: string) {
  i18n.global.locale.value = code as typeof i18n.global.locale.value
  localStorage.setItem('pw-locale', code)
  document.documentElement.lang = code
}
